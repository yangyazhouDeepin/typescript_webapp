import Vue from 'vue';
import Router from 'vue-router';

Vue.use(Router);

export default new Router({
    mode: 'history',
    base: process.env.BASE_URL,
    routes: [
        {
            path: '/',
            name: 'home',
            component: () => import('./views/home/Home.vue'),
            children: [
                {
                    path: '/',
                    name: 'football',
                    component: () => import('./views/home/Football.vue')
                },
                {
                    path: '/basketball',
                    name: 'basketball',
                    component: () => import('./views/home/Basketball.vue')
                }
            ]
        }
    ]
})

